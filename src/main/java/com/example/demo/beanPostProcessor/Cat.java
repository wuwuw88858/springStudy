package com.example.demo.beanPostProcessor;

import org.springframework.stereotype.Component;

@Component
public class Cat {
    String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Cat{" +
                "name='" + name + '\'' +
                '}';
    }
}
